const images = {}

function updateImageReferences(html) {
  return html.replaceAll(/__ASTRO_IMAGE_="([^"]+)"/gm, (full, imagePath) =>
    spreadAttributes({
      src: images[imagePath].src,
      ...images[imagePath].attributes,
    })
  )
}

const html = updateImageReferences('<p>Hi! My name is Melanie.</p>')

const frontmatter = {
  draft: true,
  date: '2069-04-20T07:57:24.796Z',
  title: 'Template',
  tags: ['link', 'article', 'video', 'post', 'etc'],
}
const file =
  '/Users/melanie/Developer/melkat-blog/src/content/posts/template.md'
const url = undefined
function rawContent() {
  return '\nHi! My name is Melanie.\n'
}
function compiledContent() {
  return html
}
function getHeadings() {
  return []
}

const Content = createComponent((result, _props, slots) => {
  const { layout, ...content } = frontmatter
  content.file = file
  content.url = url

  return renderTemplate`${unescapeHTML(html)}`
})
Content[Symbol.for('astro.needsHeadRendering')] = true

export {
  Content,
  compiledContent,
  Content as default,
  file,
  frontmatter,
  getHeadings,
  images,
  rawContent,
  url,
}
